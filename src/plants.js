export function getSpeciesImages(species: Species) {
  const imgs = [];
  for (let k in species_images) {
    if (species.name == null) continue;
    if (k.indexOf(species.name.toLowerCase().replace(/ /g, '_')) === 0) {
      imgs.push(species_images[k]);
    }
  }
  return imgs;
}

